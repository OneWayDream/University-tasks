public class MLinkedListException extends Exception {
    public MLinkedListException() {
    }

    public MLinkedListException(String message) {
        super(message);
    }

    public MLinkedListException(String message, Throwable cause) {
        super(message, cause);
    }

    public MLinkedListException(Throwable cause) {
        super(cause);
    }
}
